package variable;

public class finalexample1 {

	public static void main(String[] args) 
	{
		System.out.println("Program Starts....");
		
		int age=25;
		System.out.println("age= "+age);
		
		age=30;//re-initialization
		System.out.println("age= "+age);
		
		age=50;//re-initialization
		System.out.println("age= "+age);
		
		System.out.println("Program Ends...");
	}
}